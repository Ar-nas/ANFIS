    private void viewClass(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewClass
        // TODO add your handling code here:
        ((DefaultTableModel)jTable2.getModel()).setRowCount(0);
        MetaData metadata = exec.getMetaData();
        for (int i = 0; i < metadata.nClass(); i++) {
            String label = metadata.getClassLabelFromIndex(i);
            ArrayList<Integer> code = metadata.getClassCodeFromIndex(i);
            String codeS = new String();
            for (Integer c : code) {
                codeS = codeS.concat(c + "/");
            }
            codeS = codeS.substring(0,codeS.length()-1);
            int nTr = metadata.nDataTraining(i);
            int nTs = metadata.nDataTesting(i);
            ((DefaultTableModel)jTable2.getModel()).addRow(
                new Object[]{i, label, codeS, nTr, nTs}
            );
        }
        jFrame1.setVisible(true);
    }//GEN-LAST:event_viewClass

